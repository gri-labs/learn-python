lista = [1, 2, 3, 4]

# Agrega el elemento 5 al final de la lista
lista.append(5)

# Imprime la lista.
print(lista)

# El resultado es:
# [1, 2, 3, 4, 5]

# Agrega los elementos de la lista lista_2 a la lista lista.
lista_2 = [6, 7, 8]
lista.extend(lista_2)

# Imprime la lista.
print(lista)

# El resultado es:
# [1, 2, 3, 4, 5, 6, 7, 8]
